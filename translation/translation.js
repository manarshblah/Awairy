  const defaultLanguage = 'en'; // Set the default language
  let currentLanguage = defaultLanguage; // Initialize the current language
  

function translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach((element) => {
      const translationKey = element.getAttribute('data-translate');
      element.innerHTML = translations[currentLanguage][translationKey];
    });
  }
  


function changeLanguage(language) {
    currentLanguage = language;
    translatePage();
  }
  
  // Example of how to change the language:
  document.getElementById('english-button').addEventListener('click', () => {
    changeLanguage('en');
  });
  
  document.getElementById('arabic-button').addEventListener('click', () => {
    changeLanguage('ar');
  });
  
  // Initially translate the page to the default language
  translatePage();
  